<p>開発のきっかけ</p>
<p>インターンで出社したときにランチを出前館で注文していただいているのですが、食べたいものある？と聞かれてめちゃくちゃ悩んでしまうので、ランダムで決めたらいいんじゃん？と作りました。

</p>
<p>インターン先の研修でTypeScriptをやったので、TypeScriptフレームワークのsvelteを使って実装しています。

<p>必要最低限の機能、デザインで、いらすとやの絵がかわいい作品です。</p>
<p>もう少しいろいろ機能を付けてもいいのかなと思いつつ、放置しています🐢</p>
<p>機能紹介</p>
<p>アクセスすると、ランダムで料理のジャンルを表示します。</p>
<p>このジャンルにしよう！となったら、出前館はこちらのボタンを押すことで、出前館のサイト（インターン先のオフィス付近の該当ジャンルのお店一覧）に遷移します。</p>
<p>このジャンルの気分じゃないと思ったら別の料理を選ぶボタンを押し、再度ランダムで表示させることができます。</p>
